void init_display(uint8_t ext_vcc, uint8_t reset)
{
  if(reset) {
    HAL_GPIO_WritePin(DISPLAY_RST_GPIO_Port, DISPLAY_RST_Pin, GPIO_PIN_SET);
    HAL_GPIO_WritePin(DISPLAY_CS_GPIO_Port, DISPLAY_CS_Pin, GPIO_PIN_SET);
    // VDD (3.3V) goes high at start, lets just chill for a ms
    vTaskDelay(pdMS_TO_TICKS(1));
    // bring reset low
    HAL_GPIO_WritePin(DISPLAY_RST_GPIO_Port, DISPLAY_RST_Pin, GPIO_PIN_RESET);
    // wait 10ms
    vTaskDelay(pdMS_TO_TICKS(10));
    // bring out of reset
    HAL_GPIO_WritePin(DISPLAY_RST_GPIO_Port, DISPLAY_RST_Pin, GPIO_PIN_SET);
  }

  // Init sequence
  ssd1306_command(SSD1306_DISPLAYOFF);                    // 0xAE
  ssd1306_command(SSD1306_SETDISPLAYCLOCKDIV);            // 0xD5
  ssd1306_command(0x80);                                  // the suggested ratio 0x80

  ssd1306_command(SSD1306_SETMULTIPLEX);                  // 0xA8
  ssd1306_command(SSD1306_LCDHEIGHT - 1);

  ssd1306_command(SSD1306_SETDISPLAYOFFSET);              // 0xD3
  ssd1306_command(0x0);                                   // no offset
  ssd1306_command(SSD1306_SETSTARTLINE | 0x0);            // line #0
  ssd1306_command(SSD1306_CHARGEPUMP);                    // 0x8D

  if(ext_vcc) {
    ssd1306_command(0x10);
  }
  else {
    ssd1306_command(0x14);
  }

  ssd1306_command(SSD1306_MEMORYMODE);                    // 0x20
  ssd1306_command(0x00);                                  // 0x0 act like ks0108
  ssd1306_command(SSD1306_SEGREMAP | 0x1);
  ssd1306_command(SSD1306_COMSCANDEC);

 #if defined SSD1306_128_32
  ssd1306_command(SSD1306_SETCOMPINS);                    // 0xDA
  ssd1306_command(0x02);
  ssd1306_command(SSD1306_SETCONTRAST);                   // 0x81
  ssd1306_command(0x8F);

#elif defined SSD1306_128_64
  ssd1306_command(SSD1306_SETCOMPINS);                    // 0xDA
  ssd1306_command(0x12);
  ssd1306_command(SSD1306_SETCONTRAST);                   // 0x81

  if(ext_vcc) {
    ssd1306_command(0x9F);
  }
  else {
    ssd1306_command(0xCF);
  }

#endif

  ssd1306_command(SSD1306_SETPRECHARGE);                  // 0xd9

  if (ext_vcc) {
    ssd1306_command(0x22);
  }
  else {
    ssd1306_command(0xF1);
  }

  ssd1306_command(SSD1306_SETVCOMDETECT);                 // 0xDB
  ssd1306_command(0x40);
  ssd1306_command(SSD1306_DISPLAYALLON_RESUME);           // 0xA4
  ssd1306_command(SSD1306_NORMALDISPLAY);                 // 0xA6

  ssd1306_command(SSD1306_DEACTIVATE_SCROLL);

  ssd1306_command(SSD1306_DISPLAYON);//--turn on oled panel
}

void ssd1306_command(uint8_t cmd)
{
  HAL_GPIO_WritePin(DISPLAY_CS_GPIO_Port, DISPLAY_CS_Pin, GPIO_PIN_SET);
  HAL_GPIO_WritePin(DISPLAY_DC_GPIO_Port, DISPLAY_DC_Pin, GPIO_PIN_RESET);
  HAL_GPIO_WritePin(DISPLAY_CS_GPIO_Port, DISPLAY_CS_Pin, GPIO_PIN_RESET);
  HAL_SPI_Transmit(&hspi3, &cmd, 1, 0xFFFFFF);
  HAL_GPIO_WritePin(DISPLAY_CS_GPIO_Port, DISPLAY_CS_Pin, GPIO_PIN_SET);
}


// the most basic function, set a single pixel
void draw_pixel(uint16_t x, uint16_t y, uint16_t color)
{
  // make sure coordinates are in range
  if((x <= SSD1306_LCDWIDTH) && (y <= SSD1306_LCDHEIGHT)) {
    // x is which column
    switch (color) {

    case WHITE:
      buffer[x + (y >> 3) * SSD1306_LCDWIDTH] |=  (1 << (y & 7));
      break;

    case BLACK:
      buffer[x + (y >> 3) * SSD1306_LCDWIDTH] &= ~(1 << (y & 7));
      break;

    case INVERSE:
      buffer[x + (y >> 3) * SSD1306_LCDWIDTH] ^=  (1 << (y & 7));
      break;
    }
  }
}

void draw_vline(uint16_t x, uint16_t y2, uint16_t y1, uint16_t color)
{
  int16_t y = 0;

  // make sure y2 >= y1
  if(y1 > y2) {
    y = y1;
    y1 = y2;
    y2 = y;
  }

  // make sure coordinates are in range
  if(y2 < SSD1306_LCDHEIGHT && x < SSD1306_LCDWIDTH) {
    // x is which column
    switch (color) {

    case WHITE:
      for(y = y1; y <= y2; y++) {
        buffer[x + (y >> 3) * SSD1306_LCDWIDTH] |=  (1 << (y & 7));
      }
      break;

    case BLACK:
      for(y = y1; y <= y2; y++) {
        buffer[x + (y >> 3) * SSD1306_LCDWIDTH] &= ~(1 << (y & 7));
      }
      break;

    case INVERSE:
      for(y = y1; y <= y2; y++) {
        buffer[x + (y >> 3) * SSD1306_LCDWIDTH] ^=  (1 << (y & 7));
      }
      break;
    }
  }
}

